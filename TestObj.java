
public class TestObj implements Comparable<TestObj>{
	
	private int m_val;
	
	//////////////////////////////////////////////////////
	public TestObj(int val){
		m_val = val;
	}
	//////////////////////////////////////////////////////
	public TestObj(TestObj obj){
		m_val = obj.getVal();		
	}
	///////////////////////////////////////////////////////
	public int compareTo(TestObj obj){
		SortBenchmarking.incrementCompareCounter();
		return (m_val - obj.getVal());
	}
	///////////////////////////////////////////////////////
	public int getVal(){
		return m_val;
	}
	///////////////////////////////////////////////////////
	public String toString(){
		return Integer.toString(m_val);
	}
	///////////////////////////////////////////////////////
}
